package mediation.helper.cubiad.NativeAdView;

public class CubiInterstitialAd {
    String interstitialAdTitle;
    String interstitialAdvertiserName;
    String interstitialAdBodyText;
    String interstitialSquareIconUrl;
    String interstitialFeatureIconUrl;
    String interstitialAdUrlLink;
    String interstitialAdCallToActionData;
    String interstitialAdRating;
    String interstitialAdFeedback;


    public String getInterstitialAdTitle() {
        return interstitialAdTitle;
    }

    public void setInterstitialAdTitle(String interstitialAdTitle) {
        this.interstitialAdTitle = interstitialAdTitle;
    }

    public String getInterstitialAdvertiserName() {
        return interstitialAdvertiserName;
    }

    public void setInterstitialAdvertiserName(String interstitialAdvertiserName) {
        this.interstitialAdvertiserName = interstitialAdvertiserName;
    }

    public String getInterstitialAdBodyText() {
        return interstitialAdBodyText;
    }

    public void setInterstitialAdBodyText(String interstitialAdBodyText) {
        this.interstitialAdBodyText = interstitialAdBodyText;
    }

    public String getInterstitialSquareIconUrl() {
        return interstitialSquareIconUrl;
    }

    public void setInterstitialSquareIconUrl(String interstitialSquareIconUrl) {
        this.interstitialSquareIconUrl = interstitialSquareIconUrl;
    }

    public String getInterstitialFeatureIconUrl() {
        return interstitialFeatureIconUrl;
    }

    public void setInterstitialFeatureIconUrl(String interstitialFeatureIconUrl) {
        this.interstitialFeatureIconUrl = interstitialFeatureIconUrl;
    }

    public String getInterstitialAdUrlLink() {
        return interstitialAdUrlLink;
    }

    public void setInterstitialAdUrlLink(String interstitialAdUrlLink) {
        this.interstitialAdUrlLink = interstitialAdUrlLink;
    }

    public String getInterstitialAdCallToActionData() {
        return interstitialAdCallToActionData;
    }

    public void setInterstitialAdCallToActionData(String interstitialAdCallToActionData) {
        this.interstitialAdCallToActionData = interstitialAdCallToActionData;
    }

    public String getInterstitialAdRating() {
        return interstitialAdRating;
    }

    public void setInterstitialAdRating(String interstitialAdRating) {
        this.interstitialAdRating = interstitialAdRating;
    }

    public String getInterstitialAdFeedback() {
        return interstitialAdFeedback;
    }

    public void setInterstitialAdFeedback(String interstitialAdFeedback) {
        this.interstitialAdFeedback = interstitialAdFeedback;
    }
}
