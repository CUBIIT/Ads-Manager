package mediation.helper.cubiad.NativeAdView;

public class GeneralInfo {
    String bannerAdPriority;
    String interstitialAdPriority;
    String nativeAdPriority;
    String interstitialAdFrequency;
    String interstitialAdTimer;

    public void setBannerAdPriority(String bannerAdPriority) {
        this.bannerAdPriority = bannerAdPriority;
    }

    public String getBannerAdPriority() {
        return bannerAdPriority;
    }

    public void setInterstitialAdPriority(String interstitialAdPriority) {
        this.interstitialAdPriority = interstitialAdPriority;
    }

    public String getInterstitialAdPriority() {
        return interstitialAdPriority;
    }

    public void setNativeAdPriority(String nativeAdPriority) {
        this.nativeAdPriority = nativeAdPriority;
    }

    public String getNativeAdPriority() {
        return nativeAdPriority;
    }

    public void setInterstitialAdFrequency(String interstitialAdFrequency) {
        this.interstitialAdFrequency = interstitialAdFrequency;
    }

    public String getInterstitialAdFrequency() {
        return interstitialAdFrequency;
    }


    public void setInterstitialAdTimer(String interstitialAdTimer) {
        this.interstitialAdTimer = interstitialAdTimer;
    }

    public String getInterstitialAdTimer() {
        return interstitialAdTimer;
    }
}
